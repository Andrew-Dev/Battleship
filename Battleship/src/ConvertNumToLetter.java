public class ConvertNumToLetter {

    public char numToLetter(int number) {
        if (number == 0) {
            return 'A';
        } else if (number == 1) {
            return 'B';
        } else if (number == 2) {
            return 'C';
        } else if (number == 3) {
            return 'D';
        } else if (number == 4) {
            return 'E';
        } else if (number == 5) {
            return 'F';
        } else if (number == 6) {
            return 'G';
        } else if (number == 7){
            return 'H';
        }
    }
}
